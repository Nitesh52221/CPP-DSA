// Problem : Arthmatic Operators
// Addition : +
// Subtraction : -
// Multiplication : *
// Division : /
// Modulus : %
#include <iostream>
using namespace std;
int main()
{
    // write your code here
    int num1 = 5, num2 = 5;

    cout << "Arithmatic Operators" << endl;
    cout << (num1 + num2) << endl; // 10
    cout << (num1 - num2) << endl; // 0
    cout << (num1 * num2) << endl; // 25
    cout << (num1 / num2) << endl; // 1
    cout << (num1 % num2) << endl; // 0

    return 0;
}